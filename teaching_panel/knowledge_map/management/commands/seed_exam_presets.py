class Command(BaseCommand):
    help = 'Seed preset exam structures with topics'

    def handle(self, *args, **options):
        ege, _ = ExamType.objects.get_or_create(
            slug='ege', defaults={'name': 'ЕГЭ', 'description': 'Единый государственный экзамен', 'is_preset': True}
        )
        oge, _ = ExamType.objects.get_or_create(
            slug='oge', defaults={'name': 'ОГЭ', 'description': 'Основной государственный экзамен', 'is_preset': True}
        )

        self._seed_subject(ege, 'math-profile', 'Математика (профиль)', 'calculator', EGE_MATH_PROFILE_TOPICS, 1)
        self._seed_subject(ege, 'russian', 'Русский язык', 'pen-tool', EGE_RUSSIAN_TOPICS, 2)
        self._seed_subject(ege, 'english', 'Английский язык', 'globe', EGE_ENGLISH_TOPICS, 3)
        self._seed_subject(oge, 'math', 'Математика', 'calculator', OGE_MATH_TOPICS, 1)

        self.stdout.write(self.style.SUCCESS('Exam presets seeded successfully'))

    def _seed_subject(self, exam_type, slug, name, icon, topics_data, order):
        subj, _ = Subject.objects.get_or_create(
            exam_type=exam_type, slug=slug,
            defaults={'name': name, 'icon': icon, 'order': order}
        )
        for number, title, max_points in topics_data:
            Topic.objects.get_or_create(
                subject=subj, number=number,
                defaults={'title': title, 'max_points': max_points, 'order': number}
            )
        self.stdout.write(f'  {exam_type.name} / {name}: {len(topics_data)} topics')
